    private void saveUserInformation() {
        mName = mNameField.getText().toString();
        mPhone = mPhoneField.getText().toString();
        mCar = mCarField.getText().toString();
        if (mName != null && mPhone != null && mCar != null) {

            int selectId = mRadioGroup.getCheckedRadioButtonId();

            final RadioButton radioButton = findViewById(selectId);
            if (radioButton != null) {
                if (radioButton.getText() == null) {
                    return;
                }

                mService = radioButton.getText().toString();
                if (mService != null) {
                    Map userInfo = new HashMap();
                    userInfo.put("name", mName);
                    userInfo.put("phone", mPhone);
                    userInfo.put("car", mCar);
                    userInfo.put("service", mService);
                    mTraderDatabase.updateChildren(userInfo);

                    if (resultUri != null) {
                        FirebaseUser user = mAuth.getCurrentUser();
                        if (user != null) {
                            traderID = "";
                            traderID = user.getUid();
                            StorageReference filePath = FirebaseStorage.getInstance().getReference().child("profile_images").child(traderID);
                            Bitmap bitmap = null;
                            try {
                                bitmap = MediaStore.Images.Media.getBitmap(getApplication().getContentResolver(), resultUri);
                            } catch (IOException e) {
                                e.printStackTrace();
                            }

                            ByteArrayOutputStream baos = new ByteArrayOutputStream();
                            bitmap.compress(Bitmap.CompressFormat.JPEG, 20, baos);
                            byte[] data = baos.toByteArray();
                            UploadTask uploadTask = filePath.putBytes(data);

                            uploadTask.addOnFailureListener(new OnFailureListener() {
                                @Override
                                public void onFailure(@NonNull Exception e) {
                                    finish();
                                    return;
                                }
                            });
                            uploadTask.addOnSuccessListener(new OnSuccessListener<UploadTask.TaskSnapshot>() {
                                @Override
                                public void onSuccess(UploadTask.TaskSnapshot taskSnapshot) {
                                    Task<Uri> downloadUrl = taskSnapshot.getStorage().getDownloadUrl();

                                    Map newImage = new HashMap();
                                    newImage.put("profileImageUrl", downloadUrl.toString());
                                    mTraderDatabase.updateChildren(newImage);

                                    finish();
                                    return;
                                }
                            });
                        } else {
                            finish();
                        }

                    }
                } else {

                    Toast.makeText(getApplicationContext(), "Please provide details", Toast.LENGTH_LONG).show();
                }

            }
        }
    }
